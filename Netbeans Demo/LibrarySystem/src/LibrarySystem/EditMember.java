    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
        // TODO add your handling code here:
        jTextField1.setEnabled(false);
        int mid = Integer.parseInt(jTextField1.getText());
        String sql="select * from master_tbl_member where mid=?";
        try
        {
            c = ConnectionManager.getConnection();
            ps = c.prepareStatement(sql);
            ps.setInt(1, mid);
            ResultSet rs = ps.executeQuery();
            while(rs.next())
            {
                jTextField2.setText(rs.getString("mname"));
                jTextField3.setText(rs.getString("catagory"));
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error " + e.getMessage(),"Error",0);
        }
    }//GEN-LAST:event_jTextField1FocusLost

