    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int mid = Integer.parseInt(jTextField1.getText());
        String mname = jTextField2.getText();
        String cat = jTextField3.getText();
        int reply = JOptionPane.showConfirmDialog(null, "Are you sure?");
        if(reply == JOptionPane.YES_OPTION)
        {
            String sql = "update master_tbl_member set mname=?,catagory=? where mid=?";
         try
            {
                c=ConnectionManager.getConnection();
                ps=c.prepareStatement(sql);
                ps.setString(1, mname);
                ps.setString(2, cat);
                ps.setInt(3, mid);
                int n=ps.executeUpdate();
                if(n==1)
                {
                    JOptionPane.showMessageDialog(null, "Updated Successfully","Success", 1);
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField1.setEnabled(true);
                }
               
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,"Error " + e.getMessage(),"Error",0);
            }
        }
         else
                {
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField1.setEnabled(true);
                }
    }//GEN-LAST:event_jButton1ActionPerformed

